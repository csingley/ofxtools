COUNTRY_CODES = ISO3166_1a3


class CURRENCY(Aggregate):
    """OFX section 5.2"""

    currate = Decimal(required=True)
    cursym = OneOf(*CURRENCY_CODES, required=True)


class ORIGCURRENCY(Aggregate):
    """OFX section 5.2"""

    currate = Decimal(required=True)
    cursym = OneOf(*CURRENCY_CODES, required=True)


class Origcurrency:
    """Mixin providing property aliases and CURRENCY/ORIGCURRENCY mutex"""

    optionalMutexes: Sequence[Sequence[str]] = [["currency", "origcurrency"]]

    @property
    def curtype(self):
        cur = self.currency
        if cur is None:
            cur = self.origcurrency
        if cur is not None:
            return cur.__class__.__name__

    @property
    def cursym(self):
        cur = self.currency
        if cur is None:
            cur = self.origcurrency
        if cur is not None:
            return ISO4217_CURSYM_BY_NUMBER.get(cur.cursym, cur.cursym)

    @property
    def currate(self):
        cur = self.currency
        if cur is None:
            cur = self.origcurrency
        if cur is not None:
            return cur.currate
